#include <stdio.h>
int main()
{
    printf("Hello\nWorld\n\n"); // \n is used to print new line
    // Hello
    // World

    printf("Hello\tWorld\n\n"); // \t is used to print horizontal tab
    // Hello   World

    printf("Hello\vWorld\n\n"); // \v is used to print vertical tab
    // Hello
    //      World

    printf("\a"); // \a is used to print alert sound

    printf("\\ is getting printed\n"); // \\ is used to print backslash

    printf("\\n is used to print new line\n"); // \n is used to print new line

    printf("This is 'Alik'\n"); // This is 'Alik'

    printf("This is \"Alik\"\n"); // This is "Alik"

    return 0;
}